# Declaração
minha_Tupla = (1, 2, 3, 4, 5)
print("Minha Tupla:", minha_Tupla)

# Exibindo apenas o primeiro elemento
print("Primeiro elemento:", minha_Tupla[0])

# Exibindo o último elemento
print("Último elemento:", minha_Tupla[-1])

# count
print("Quantidade de vezes que o número 2 aparece:", minha_Tupla.count(2))

# index
print("Índice do número 3:", minha_Tupla.index(3))

