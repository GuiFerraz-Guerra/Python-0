# Condição Verdadeira
if True:
    print("Verdadeiro")
else:
    print("Falso")

# Condição Falsa
if False:
    print("Falso")
else:
    print("Verdadeiro")

# Operadores lógicos: and, or, not
if True and True:
    print("Ambos são verdadeiros")

if True or False:
    print("Pelo menos um é verdadeiro")

if not False:
    print("A negação de falso é verdadeiro")

    


